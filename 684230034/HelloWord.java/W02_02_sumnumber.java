public class W02_02_sumnumber {
    public static void main(String[] args) {
        // แสดงผลตัวแปรเลขสองหลัก
        int x = 15; // ค่าX
        int y = 20; // ค่าY
        int sum = x+y; // ผลรวมค่าX+Y
        
        // ผลรวมตัวแปรเลขสองหลัก
        System.out.println("ผลรวมของตัวเลข " + x + "+" + y + "=" + sum);
        System.out.println("ผลรวมของ " + x + " + " + y + " = " + sum);
    }
}
