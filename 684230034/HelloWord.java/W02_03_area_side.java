public class W02_03_area_side {
        public static void main(String[] args) {
            int side = 15; // ความยาวด้านของสี่เหลี่ยมจัตุรัส
            int area = side * side; // คำนวณพื้นที่โดยการยกกำลังสองค่า

            // แสดงผล
            System.out.println("พื้นที่สี่เหลี่ยมจัตุรัสที่มีด้าน = " +side+ " คือ " + area);
        }
}